#!/bin/bash

plotDir="../vis"

start="RANDOM"
stop="Visiting"
delay="10"

function visualize {
	gt=$1
	gp=$2
	st=$3
	sp=$4
	cost=$5
	resource=$6
	java -jar vis.jar $plotDir/$pre/ $gt $gp $st $sp $start $stop $cost $resource $delay
	echo ""
	echo ""
	echo ""
}

function vis {
	for algo in ${algorithms[@]}; do
		visualize $1 $2 $algo __ $3 $4
	done
}

algorithms=(BFS DFS RANDOM_WALK RANDOM_WALK_NR UNIFORM GREEDY_ORACLE MOD)

